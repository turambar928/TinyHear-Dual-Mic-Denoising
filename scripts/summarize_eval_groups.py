#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any


GROUPS = [
    ("low_snr", None, 0.0),
    ("mid_snr", 0.0, 10.0),
    ("high_snr", 10.0, None),
]


def group_name(noisy_si_sdr: float) -> str:
    for name, lower, upper in GROUPS:
        if lower is not None and noisy_si_sdr < lower:
            continue
        if upper is not None and noisy_si_sdr >= upper:
            continue
        return name
    raise RuntimeError(f"unmatched SI-SDR value: {noisy_si_sdr}")


def mean(rows: list[dict[str, Any]], key: str) -> float:
    if not rows:
        return 0.0
    return sum(float(row[key]) for row in rows) / len(rows)


def summarize(rows: list[dict[str, Any]], worst: int) -> dict[str, Any]:
    groups = {}
    for name, _, _ in GROUPS:
        group_rows = [row for row in rows if row["snr_group"] == name]
        regressions = [row for row in group_rows if row["si_sdr_improvement"] < 0.0]
        groups[name] = {
            "items": len(group_rows),
            "mean_noisy_si_sdr": mean(group_rows, "noisy_si_sdr"),
            "mean_enhanced_si_sdr": mean(group_rows, "enhanced_si_sdr"),
            "mean_si_sdr_improvement": mean(group_rows, "si_sdr_improvement"),
            "regressions": len(regressions),
            "regression_rate": len(regressions) / len(group_rows) if group_rows else 0.0,
        }
    worst_rows = sorted(rows, key=lambda row: row["si_sdr_improvement"])[:worst]
    return {
        "items": len(rows),
        "groups": groups,
        "worst_cases": worst_rows,
    }


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("metrics", help="metrics.json generated by scripts/evaluate.py")
    parser.add_argument("--out", help="Optional path to write grouped summary JSON.")
    parser.add_argument("--worst", type=int, default=12)
    args = parser.parse_args()

    metrics_path = Path(args.metrics)
    payload = json.loads(metrics_path.read_text(encoding="utf-8"))
    rows = payload["items"]
    for row in rows:
        row["snr_group"] = group_name(float(row["noisy_si_sdr"]))
    summary = summarize(rows, args.worst)
    text = json.dumps(summary, indent=2)
    print(text)
    if args.out:
        Path(args.out).parent.mkdir(parents=True, exist_ok=True)
        Path(args.out).write_text(text + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()
